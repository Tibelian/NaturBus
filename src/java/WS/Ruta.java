
package WS;

public class Ruta {

    private String origen;
    private String destino;
    private String fecha;
    private String horaSalida;
    private String plazasDisponibles;
    private String plazasTotales;
    private String precio;

    public Ruta() {
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHoraSalida() {
        return horaSalida;
    }

    public void setHoraSalida(String horaSalida) {
        this.horaSalida = horaSalida;
    }

    public String getPlazasDisponibles() {
        return plazasDisponibles;
    }

    public void setPlazasDisponibles(String plazasDisponibles) {
        this.plazasDisponibles = plazasDisponibles;
    }

    public String getPlazasTotales() {
        return plazasTotales;
    }

    public void setPlazasTotales(String plazasTotales) {
        this.plazasTotales = plazasTotales;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }
    
}
